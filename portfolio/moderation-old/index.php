                  </div>
                  <div class="wpb_column columns medium-8 thb-dark-column small-12">
                      <p>With this new redesign, Linkedin trust want to change the poor interaction design, lack of availability of information for decisions and unstructured information architecture coupled with tedious manual processes has limited the true potential of content moderators and Leaders & partners.</p>
