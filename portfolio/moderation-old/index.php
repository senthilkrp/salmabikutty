                      <h4>Project Summary</h4>
                  </div>
                  <div class="wpb_column columns medium-8 thb-dark-column small-12">
                      <p>Inspector is one of the most critical tools used by GCO and TNS organisations for resolving member support requests and moderation of member reported content and profiles which are violating LinkedIn terms of service respectively. More than 2000 employees access the Inspector on a daily basis and this includes Contractors and Full-time employees.</p>

                        <p>This project is to address the inefficiencies in our current tools used by reviewers to monitor our platform for unprofessional and harmful activities such as spam, scam or harassment, reviewing the quality of ads, jobs, and reviewing potential fake accounts.</p>

                  </div>
                    
