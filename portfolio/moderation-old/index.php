                      <ol>
                          <li>Reduce the manual work for reviewers and organise the information for efficient content moderation </li>
                          <li>Reduce the mental stress caused to human reviewers by moderation on sensitive content</li>
                          <li>Make the platform reliable, consistent and effective for content moderation and integration</li>

                      </ol>
                      
